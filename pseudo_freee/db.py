"""疑似freee のデータアクセス層（A-8 永続化: SQLite / Postgres 両対応）。

在庫 inventory_dashboard/db.py を**独立移植**したもの（各アプリは単体で動く方針=NFR-5）。
DATABASE_URL が postgres:// なら Neon 等の Postgres(psycopg3)、無ければローカル SQLite。
呼び出し側(app.py)は常に '?' プレースホルダで SQL を書き、Postgres のときだけ '%s' に変換する。

方言差の吸収ポイント:
  - プレースホルダ : '?'(SQLite) ⇄ '%s'(psycopg)              … _Connection.execute
  - 採番          : lastrowid(SQLite) ⇄ INSERT ... RETURNING id … insert_returning_id
  - DDL          : AUTOINCREMENT / REAL ⇄ IDENTITY / DOUBLE PRECISION … *_SCHEMA_SQL
  - created_at    : 両方言とも **TEXT** に統一。疑似freee は stdlib の json.dumps で API 応答を
                    作るため、Postgres の timestamp 型(datetime)を返すと壊れる。Postgres は
                    to_char(now(),'YYYY-MM-DD HH24:MI:SS') で SQLite と同形式の文字列にする。
  - 重複無視      : INSERT ... ON CONFLICT DO NOTHING（SQLite/Postgres 双方で動く＝app.py側で統一）

秘密情報（DATABASE_URL）は環境変数のみ。コード/ログには残さない。
"""

from __future__ import annotations

