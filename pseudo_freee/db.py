def reset_tables(conn: _Connection) -> None:
    """全テーブルを DROP する（テスト/テスト用ブランチ専用の破壊的操作）。
    本番DBに対しては絶対に呼ばないこと（EVOLUTION_PLAN.md「テストDBの分離」）。"""
    if conn.postgres:
        for table in PSEUDO_FREEE_TABLES:
            conn.execute(f"DROP TABLE IF EXISTS {table} CASCADE")
    else:
        for table in PSEUDO_FREEE_TABLES:
            conn.execute(f"DROP TABLE IF EXISTS {table}")


def table_exists(conn: _Connection, table: str) -> bool:
    if conn.postgres:
        row = conn.execute(
            "SELECT 1 FROM information_schema.tables WHERE table_name = ?",
            (table,),
        ).fetchone()
        return row is not None
    row = conn.execute(
        "SELECT name FROM sqlite_master WHERE type = 'table' AND name = ?",
        (table,),
    ).fetchone()
    return row is not None


def has_column(conn: _Connection, table: str, column: str) -> bool:
    """table に column が存在するか（方言差を吸収）。table/column は定数のみを渡すこと。"""
    if conn.postgres:
        row = conn.execute(
            "SELECT 1 FROM information_schema.columns WHERE table_name = ? AND column_name = ?",
            (table, column),
        ).fetchone()
        return row is not None
    rows = conn.execute(f"PRAGMA table_info({table})").fetchall()
    return any(row["name"] == column for row in rows)


def insert_returning_id(conn: _Connection, sql: str, params: Sequence[Any]) -> int:
    """INSERT して採番された id を返す（lastrowid と RETURNING の差を吸収）。

    sql は末尾に RETURNING を付けない INSERT ... VALUES (...) を渡す（セミコロン不要）。
    """
    if conn.postgres:
        cur = conn.execute(sql.rstrip().rstrip(";") + " RETURNING id", params)
        return int(cur.fetchone()["id"])
    cur = conn.execute(sql, params)
    return int(cur.lastrowid)
