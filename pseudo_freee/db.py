def database_url() -> str:
    """接続先 URL（前後空白除去・'#' 始まりは未設定扱い＝auth/storage と同じ防御）。"""
    value = os.environ.get("DATABASE_URL", "").strip()
    if value.startswith("#"):
        return ""
    return value


def is_postgres() -> bool:
    return database_url().startswith("postgres")


