"""DB アクセス層（EVOLUTION_PLAN.md A-2: Neon 移行）。

このモジュールが「接続・スキーマDDL・SQL方言差の吸収」を一手に引き受ける。
業務ロジック(app.py)は方言を意識せず、常に '?' プレースホルダで SQL を書く。

DATABASE_URL による切替:
  - 未設定         → ローカル SQLite（開発・テストの既定）
  - postgresql://  → Neon などの Postgres（psycopg3）

方言差を吸収する箇所:
  - プレースホルダ : '?'(SQLite)        ⇄ '%s'(psycopg)          … _Connection.execute
  - 採番          : lastrowid(SQLite)   ⇄ INSERT ... RETURNING id … insert_returning_id
  - 月の抽出      : strftime(SQLite)    ⇄ EXTRACT(Postgres)       … month_expr
  - DDL          : AUTOINCREMENT/REAL  ⇄ IDENTITY/DOUBLE PRECISION … *_SCHEMA_SQL

設計境界（EVOLUTION_PLAN.md「データアクセス境界」）:
  DB への読み書き・スキーマ所有はサーバ側(Python)が唯一の主体。フロントは触らない。
"""

from __future__ import annotations

import os
import sqlite3
from contextlib import contextmanager
from typing import Any, Iterator, Sequence

# 業務ロジックの型注釈に使うエイリアス。実体は _Connection（SQLite/psycopg 両対応の薄いラッパ）。
Connection = Any


# ---------------------------------------------------------------------------
# バックエンド判定（DATABASE_URL を都度読むので、テストで環境変数を差し替えられる）
# ---------------------------------------------------------------------------
def database_url() -> str:
    return os.environ.get("DATABASE_URL", "").strip()


def is_postgres() -> bool:
    url = database_url()
    return url.startswith("postgres://") or url.startswith("postgresql://")


# ---------------------------------------------------------------------------
# スキーマ DDL（SQLite 版 / Postgres 版を併記）
# ---------------------------------------------------------------------------
# 2つの DDL は「同じ論理スキーマ」を表す。列を足すときは必ず両方を更新すること。
# 差分は方言のみ:
#   INTEGER PRIMARY KEY AUTOINCREMENT → INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY
#   REAL                              → DOUBLE PRECISION
#   created_at/updated_at の TEXT      → TIMESTAMP（CURRENT_TIMESTAMP をそのまま代入できる）
#   PRAGMA foreign_keys = ON           → Postgres は常に FK 強制のため不要
