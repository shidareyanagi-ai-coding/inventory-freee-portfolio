def _dict_factory(cursor: sqlite3.Cursor, row: Sequence[Any]) -> dict[str, Any]:
    """SQLite の行を dict で返す（psycopg の dict_row と戻り値の形を揃える）。"""
    return {col[0]: row[idx] for idx, col in enumerate(cursor.description)}


class _Connection:
    """SQLite / psycopg を同じ API で扱う薄いラッパ。

    - execute(sql, params): SQL は常に '?' で書き、Postgres のときだけ '%s' へ変換する。
      params が None のときはパラメータ無しで実行する（psycopg が SQL 内の '%' を
      プレースホルダと誤認しないようにするため）。
    - 戻り値は dict を返すカーソル（fetchone()/fetchall()/lastrowid を持つ）。
    """

    def __init__(self, raw: Any, postgres: bool) -> None:
        self._raw = raw
        self.postgres = postgres

    def execute(self, sql: str, params: Sequence[Any] | None = None) -> Any:
        if self.postgres:
            sql = sql.replace("?", "%s")
            cur = self._raw.cursor()
            if params is None:
                cur.execute(sql)
            else:
                cur.execute(sql, params)
            return cur
        # SQLite: Connection.execute がそのまま dict 行カーソルを返す。
        if params is None:
            return self._raw.execute(sql)
        return self._raw.execute(sql, params)

    def executemany(self, sql: str, seq_of_params: Sequence[Sequence[Any]]) -> Any:
        if self.postgres:
            sql = sql.replace("?", "%s")
            cur = self._raw.cursor()
            cur.executemany(sql, list(seq_of_params))
            return cur
        return self._raw.executemany(sql, seq_of_params)

    def executescript(self, sql: str) -> None:
        # 複数文（スキーマDDL）をまとめて実行する。どちらの方言もパラメータ無し。
        if self.postgres:
            self._raw.cursor().execute(sql)
        else:
            self._raw.executescript(sql)

    def commit(self) -> None:
        self._raw.commit()

    def rollback(self) -> None:
        self._raw.rollback()

    def close(self) -> None:
        self._raw.close()


@contextmanager
def get_conn(sqlite_path: str | os.PathLike[str] | None = None) -> Iterator[_Connection]:
    """トランザクション境界付きの接続を返す。

    正常終了で commit、例外で rollback、最後に必ず close。旧 app.get_conn と同じ契約。
    Postgres のときは sqlite_path は無視される。
    """
    if is_postgres():
        import psycopg
        from psycopg.rows import dict_row

        raw = psycopg.connect(database_url(), row_factory=dict_row)
        conn = _Connection(raw, postgres=True)
    else:
        if sqlite_path is None:
            raise ValueError("sqlite_path is required when DATABASE_URL is not a Postgres URL")
        raw = sqlite3.connect(sqlite_path)
        raw.row_factory = _dict_factory
        raw.execute("PRAGMA foreign_keys = ON")
        conn = _Connection(raw, postgres=False)

    try:
        yield conn
        conn.commit()
    except Exception:
        conn.rollback()
        raise
    finally:
        conn.close()


def create_schema(conn: _Connection) -> None:
    """方言に応じたスキーマ DDL を流す（冪等: CREATE TABLE IF NOT EXISTS）。"""
    conn.executescript(POSTGRES_SCHEMA_SQL if conn.postgres else SQLITE_SCHEMA_SQL)


def insert_returning_id(conn: _Connection, sql: str, params: Sequence[Any]) -> int:
    """INSERT して採番された id を返す（lastrowid と RETURNING の差を吸収）。"""
    if conn.postgres:
        cur = conn.execute(sql.rstrip().rstrip(";") + " RETURNING id", params)
        return int(cur.fetchone()["id"])
    cur = conn.execute(sql, params)
    return int(cur.lastrowid)


def month_expr(conn: _Connection, column: str) -> str:
    """日付テキスト列から「月(1-12)」を取り出す SQL 断片を返す。

    SQLite は strftime、Postgres は EXTRACT。column は定数（列名）のみを渡すこと。
    """
    if conn.postgres:
        return f"CAST(EXTRACT(MONTH FROM {column}::date) AS INTEGER)"
    return f"CAST(strftime('%m', {column}) AS INTEGER)"


